class CommunityFriendsPage extends StatefulWidget {
  const CommunityFriendsPage({super.key});

  @override
  State<CommunityFriendsPage> createState() => _CommunityFriendsPageState();
}

class _CommunityFriendsPageState extends State<CommunityFriendsPage> {
  List<Widget> pages = [];
  int index = 0;

  @override
  void initState() {
    super.initState();
    pages = [
      CommunityFriendsDefault(
        onPageChanged: onPageChanged,
      ),
      CommunityFriendsOnline(
        onPageChanged: onPageChanged,
      ),
      CommunityFriendsRequests(
        onPageChanged: onPageChanged,
      ),
      CommunityFriendsSuggestion(
        onPageChanged: onPageChanged,
      ),
      CommunityFriendsAllFriends(
        onPageChanged: onPageChanged,
      ),
    ];
  }

  void onPageChanged(int val) {
    setState(() {
      index = val;
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: SafeArea(
        child: Padding(
          padding: const EdgeInsets.symmetric(horizontal: 24),
          child: index == 0
              ? CommunityFriendsDefault(
                  onPageChanged: (p0) {
                    setState(() {
                      index = p0;
                    });
                  },
                )
              : pages[index],
        ),
      ),
    );
  }
}
